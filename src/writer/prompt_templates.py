# -*- coding: utf-8 -*-
"""
动态 Prompt 补丁：根据章节类型自动切换提示词模版。
战斗章：高强度动词库、快节奏短句；感情章：心理描写与氛围渲染。
"""
from typing import Dict

# 章节类型 -> 模版片段（可被注入到系统提示或用户提示末尾）
CHAPTER_TYPE_TEMPLATES: Dict[str, str] = {
    "战斗章": """
【战斗章写作要求】
- 动词优先：斩、劈、闪、掠、爆、轰、挡、格、卸。
- 短句为主，节奏紧凑，少长心理独白。
- 动作与结果成对出现，避免空洞描写。
""",
    "感情章": """
【感情章写作要求】
- 心理活动与氛围渲染占比可提高。
- 注意眼神、语气、细微动作的暗示。
- 对话可配合神态与环境（如雨声、夜色）烘托情绪。
""",
    "日常章": """
【日常章写作要求】
- 对话与动作自然，可穿插幽默或伏笔。
- 节奏适中，不必刻意短句。
""",
    "悬念章": """
【悬念章写作要求】
- 信息分层释放，可留白与暗示。
- 适当短句制造紧张感，关键处可戛然而止。
""",
    "": "",
}


def get_template_for_chapter_type(chapter_type: str) -> str:
    """根据章节类型返回对应模版，未知类型返回空字符串。"""
    return CHAPTER_TYPE_TEMPLATES.get(chapter_type, "")
